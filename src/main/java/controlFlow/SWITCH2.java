package controlFlow;

public class SWITCH2 {
    public static void main(String[] args) {
        int number=20;
        switch (number){
            case 10:
                System.out.println("Number is 10");
                break;
            case 20:
                System.out.println("Number is 20");
                break;
            case 30:
                System.out.println("Number is 30");
                break;
            default:
                System.out.println("Number is not 10, 20 or 30");
        }
    }
}
